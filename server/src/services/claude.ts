import Anthropic from "@anthropic-ai/sdk";
import {
  extractTnvedCode,
  fetchTnvedRates,
  fetchTnvedPermits,
  fetchRelevantLaws,
} from "./alta.js";

const client = new Anthropic({ apiKey: process.env.CLAUDE_API_KEY });

export async function generateArticle(
  transcriptRaw: string,
  subject: string
): Promise<{ title: string; content: string }> {
  let altaBlock = "";

  const tncode = extractTnvedCode(transcriptRaw);
  if (tncode) {
    // Have an HS code — get precise tariff data from Alta API
    const [rates, permits] = await Promise.all([
      fetchTnvedRates(tncode),
      fetchTnvedPermits(tncode),
    ]);

    if (rates) {
      const lines: string[] = [
        `\n\nДанные Alta Такса (alta.ru) для ТН ВЭД ${tncode}:`,
        rates.name ? `- Наименование товара: ${rates.name}` : "",
        rates.dutyRate ? `- Ставка ввозной таможенной пошлины: ${rates.dutyRate}` : "",
        rates.vatRate ? `- НДС: ${rates.vatRate}%` : "",
      ].filter(Boolean);

      if (permits.length > 0) {
        lines.push(`- Разрешительные документы (точные названия, используй как есть): ${permits.join("; ")}`);
      }

      lines.push(
        "\nИспользуй эти точные данные в статье. При упоминании ставки пошлины или НДС ссылайся на эти значения."
      );
      altaBlock = lines.join("\n");
    }
  } else {
    // No HS code — search tamdoc for relevant regulatory documents
    const laws = await fetchRelevantLaws(subject);
    if (laws.length > 0) {
      altaBlock =
        `\n\nАктуальные нормативные документы по теме (источник: alta.ru):\n` +
        laws.map((l, i) => `${i + 1}. ${l.title}`).join("\n") +
        `\n\nЦитируй их точные названия в статье (без ссылок — см. правило про <a> ниже).`;
    }
  }

  const lawsBlock = altaBlock;

  const message = await client.messages.create({
    model: process.env.CLAUDE_MODEL ?? "claude-sonnet-4-6",
    max_tokens: 8192,
    messages: [
      {
        role: "user",
        content: `Ты эксперт в области таможенного оформления и грузоперевозок, а также SEO-копирайтер компании "Royal Cargo" — таможенного брокера и логистической компании.
На основе транскрипта телефонного разговора напиши экспертную SEO-статью для сайта компании.

Тема звонка: ${subject}

Транскрипт:
${transcriptRaw}${lawsBlock}

Перед написанием статьи определи по расшифровке тип обращения:
- «таможенное оформление» — импорт/экспорт, пересечение границы РФ, есть код ТН ВЭД или явно упомянута таможня
- «внутрироссийская перевозка» — доставка между городами/регионами РФ без пересечения границы, без таможенных процедур
Все разделы статьи, включая «Какие документы потребуются», формируй строго под фактический тип обращения — не подмешивай таможенные документы (декларация на товары, разрешительные документы, сертификаты для целей таможни) в статью про внутрироссийскую перевозку, и наоборот.

Требования к статье:
- Язык: русский, экспертный деловой стиль — без канцелярита, без пустых рекламных клише и воды, но живой и убедительный
- Заголовок (H1) и подзаголовки (H2) — «цепляющие» и конкретные: с реальными деталями из звонка (тип груза/услуги, маршрут, страна, код ТН ВЭД) как SEO-ключами, построены как реальный поисковый запрос читателя (что + откуда/куда + что сделать/сколько стоит), без общих фраз вроде «Всё о таможенном оформлении»
- В теле статьи, не только в заголовках, естественно используй 2–3 близких по смыслу вариации ключевых фраз (синонимы, смежные формулировки того же запроса) — для смысловой SEO-релевантности, но без переспама одним и тем же словосочетанием
- Вводный абзац сразу обозначает проблему клиента и что он получит из статьи — цепляет с первых строк, но остаётся по делу
- Плотность: каждое предложение несёт новый факт, цифру, требование или конкретное действие. Без предложений-связок без информации («важно отметить», «стоит понимать», «как известно», «безусловно» и т. п.) и без пересказа уже сказанного другими словами
- Объём: 1400–1800 слов
- Структура: H1 → вводный абзац → 3–4 раздела с H2 (общая экспертная часть) → ОБЯЗАТЕЛЬНЫЙ раздел «Особенности» → ОБЯЗАТЕЛЬНЫЙ раздел «Вопрос-ответ» → ОБЯЗАТЕЛЬНЫЙ раздел «Какие документы потребуются» (или аналогичный заголовок по теме) → заключение с чётким следующим шагом для читателя
- Тон основного текста: строгий, фактический, как у практикующего эксперта по ВЭД и логистике — только конкретика, никаких оценочных прилагательных без основания
- Не упоминай имён клиентов и личных данных из звонка

Раздел «Особенности» (заголовок H2, можно адаптировать под тему — например «Особенности этой поставки»):
- Раскрывай, чем именно ЭТОТ случай отличается от типового: тип груза, страна происхождения/назначения, объём/количество, способ доставки, упаковка, сроки, любые нестандартные условия или сложности, упомянутые в транскрипте
- Не повторяй факты, уже раскрытые в других разделах статьи — здесь только то, что делает конкретно этот кейс нетиповым
- Если в транскрипте нет ничего необычного — опиши реальные типичные нюансы именно для этой категории товара/услуги (например, особенности сертификации для этой товарной группы, типичные сроки оформления для этого направления), не выдумывай подробности о звонке, которых не было
- Не добавляй населённые пункты, районы, суммы, сроки или другие конкретные данные «для примера», которых нет в транскрипте — только то, что реально прозвучало в разговоре, либо обобщённые формулировки без выдуманных подробностей

Раздел «Вопрос-ответ» (заголовок H2, вариант — «Частые вопросы»):
- Сформируй от 2 до 4 пар «вопрос — ответ» на реальные практические вопросы читателя в этой ситуации
- Если в транскрипте клиент явно задавал конкретный вопрос — включи его первым, перефразировав как обобщённый вопрос от лица читателя. Убери все идентифицирующие детали звонка: имена, название компании-клиента, телефоны, суммы контракта, номера сделок — оставь только суть вопроса (например, вместо «Могу ли я, Иван Петров из ООО Ромашка, ввезти партию 500 кг X без сертификации» пиши «Нужна ли дополнительная сертификация при ввозе такого объёма товара X»). Ответь на него фактически, опираясь на транскрипт и данные Alta Такса выше, если они есть
- Оставшиеся пары (до 2–4 в сумме) — правдоподобные вопросы, которые реально задал бы читатель в этой ситуации. Если клиент в звонке явно не задавал вопросов, все 2–4 пары формируй как правдоподобно смоделированные
- Формат каждой пары строго: <h3>Вопрос: текст вопроса?</h3><p>Текст ответа.</p> — вопрос всегда оформляй как <h3>, ответ как обычный абзац <p>, при необходимости выделяй ключевые термины в ответе через <strong>
- Ответы должны быть такими же фактическими и конкретными, как основной текст статьи — без общих фраз и без «уточните у менеджера»

Раздел «Какие документы потребуются»:
- Оформляй как маркированный список <ul><li>, каждый пункт — <strong>точное официальное название документа</strong> и короткое пояснение зачем он нужен
- Если выше в промпте переданы точные названия разрешительных документов (из данных Alta Такса/alta.ru) — используй их дословно, с номером техрегламента если он указан. Не пиши расплывчато «уточним позже», если точные данные уже есть
- Обязательно включай базовый для темы набор (например, внешнеторговый контракт, инвойс/коммерческий счёт), даже если по звонку не упоминались, если они логически нужны для этого вида оформления
- Если точных данных о разрешительных документах нет — перечисли документы, реально типичные для этой категории товара/услуги, без выдумывания номеров регламентов

Источники и нормативные акты (сквозное правило для ВСЕЙ статьи, включая разделы «Особенности» и «Вопрос-ответ»):
- Каждый раз, когда в тексте указывается конкретное юридическое требование, срок, обязанность, ставка пошлины, НДС или другой нормативный факт — эта формулировка должна быть подкреплена упоминанием конкретного нормативного акта (закон, кодекс, постановление, техрегламент, приказ и т. п.)
- Никогда не используй расплывчатые обороты вроде «согласно законодательству», «по закону», «в соответствии с нормами» без указания, что это за акт
- ССЫЛКИ ДЕЛАТЬ НЕЛЬЗЯ: не используй тег <a> нигде в статье — читатель не должен иметь возможность случайно уйти со страницы на сторонний сайт. Название документа указывай только текстом, жирным через <strong>: <strong>Название документа</strong>, без href и без target="_blank"
- Используй КАЖДЫЙ документ, переданный выше в блоке нормативных документов (если он предоставлен), минимум один раз по точному названию — распредели упоминания по разным разделам статьи, а не собирай в одном месте
- При первом упоминании акта в статье — полное точное название жирным через <strong>; при повторных упоминаниях того же акта дальше по тексту можно называть его короче, без <strong>
- Если точных данных о нормативном акте для конкретного факта нет — не выдумывай номер или название документа; ссылайся на реально существующий тип акта по теме (например, «Таможенный кодекс ЕАЭС», «Устав автомобильного транспорта», профильный техрегламент ЕАЭС), не придумывая несуществующих реквизитов

Формат ответа:
- Верни ответ строго в JSON: {"title": "...", "content": "..."}
- В поле content используй HTML-теги: <h1>, <h2>, <h3>, <p>, <strong>, <ul>, <li> (без <a> — ссылки запрещены)`,
      },
    ],
  });

  const text =
    message.content[0].type === "text" ? message.content[0].text : "";

  const jsonMatch = text.match(/\{[\s\S]*\}/);
  if (!jsonMatch) throw new Error("Claude вернул неожиданный формат");

  return JSON.parse(jsonMatch[0]) as { title: string; content: string };
}
